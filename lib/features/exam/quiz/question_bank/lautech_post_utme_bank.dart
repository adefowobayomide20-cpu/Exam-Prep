import "../quiz_question.dart";

QuizQuestion _q(String subject, String text, List<String> options, String correct, String explanation) {
  return QuizQuestion(
    subject: subject,
    text: text,
    options: options,
    correctIndex: options.indexOf(correct),
    explanation: explanation,
  );
}

const _englishSubject = "English Language";
const _mathsSubject = "Mathematics";
const _gkSubject = "General Knowledge";

QuizQuestion _eq(String text, List<String> options, String correct, String explanation) =>
    _q(_englishSubject, text, options, correct, explanation);

QuizQuestion _mq(String text, List<String> options, String correct, String explanation) =>
    _q(_mathsSubject, text, options, correct, explanation);

QuizQuestion _gq(String text, List<String> options, String correct, String explanation) =>
    _q(_gkSubject, text, options, correct, explanation);

